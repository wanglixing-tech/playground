package crs.fcl.integration.model;

public class Nutrient {
	String id;
	String name;
	String unitId;
	String unitAbbr;
	String unitName;
	String valueGroupId;
	String valueGroupName;
	String valueGroupAmountValue;
	String valueGroupReferenceIntakeValue;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUnitId() {
		return unitId;
	}
	public void setUnitId(String unitId) {
		this.unitId = unitId;
	}
	public String getUnitAbbr() {
		return unitAbbr;
	}
	public void setUnitAbbr(String unitAbbr) {
		this.unitAbbr = unitAbbr;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getValueGroupId() {
		return valueGroupId;
	}
	public void setValueGroupId(String valueGroupId) {
		this.valueGroupId = valueGroupId;
	}
	public String getValueGroupName() {
		return valueGroupName;
	}
	public void setValueGroupName(String valueGroupName) {
		this.valueGroupName = valueGroupName;
	}
	public String getValueGroupAmountValue() {
		return valueGroupAmountValue;
	}
	public void setValueGroupAmountValue(String valueGroupAmountValue) {
		this.valueGroupAmountValue = valueGroupAmountValue;
	}
	public String getValueGroupReferenceIntakeValue() {
		return valueGroupReferenceIntakeValue;
	}
	public void setValueGroupReferenceIntakeValue(String valueGroupReferenceIntakeValue) {
		this.valueGroupReferenceIntakeValue = valueGroupReferenceIntakeValue;
	}
	
}
