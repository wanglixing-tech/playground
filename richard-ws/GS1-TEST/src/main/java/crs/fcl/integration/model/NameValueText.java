package crs.fcl.integration.model;

public class NameValueText {
	String nameId;
	String name;
	String valueId;
	String value;
	String textXmlSpace;
	String text;
	public String getNameId() {
		return nameId;
	}
	public void setNameId(String nameId) {
		this.nameId = nameId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValueId() {
		return valueId;
	}
	public void setValueId(String valueId) {
		this.valueId = valueId;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getTextXmlSpace() {
		return textXmlSpace;
	}
	public void setTextXmlSpace(String textXmlSpace) {
		this.textXmlSpace = textXmlSpace;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
}
