package crs.fcl.integration.main;

public class Worktree {
	private String worktreeName;
	private String branchName;
	private String worktreeRelPath;
	private String worktreeType;
	
	public String getWorktreeName() {
		return worktreeName;
	}
	public void setWorktreeName(String worktreeName) {
		this.worktreeName = worktreeName;
	}
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public String getWorktreeRelPath() {
		return worktreeRelPath;
	}
	public void setWorktreeRelPath(String worktreeRelPath) {
		this.worktreeRelPath = worktreeRelPath;
	}
	public String getWorktreeType() {
		return worktreeType;
	}
	public void setWorktreeType(String worktreeType) {
		this.worktreeType = worktreeType;
	}
	
}
