package crs.fcl.integration.model;

public class TaggedLongTextItems {
	String id;
	String name;
	String textXmlSpace;
	String text;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTextXmlSpace() {
		return textXmlSpace;
	}
	public void setTextXmlSpace(String textXmlSpace) {
		this.textXmlSpace = textXmlSpace;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
}
