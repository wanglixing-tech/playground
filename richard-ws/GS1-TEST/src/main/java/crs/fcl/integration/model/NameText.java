package crs.fcl.integration.model;

public class NameText {
	String nameId;
	String name;
	String text;
	public String getNameId() {
		return nameId;
	}
	public void setNameId(String string) {
		this.nameId = string;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text= text;
	}
	
}
