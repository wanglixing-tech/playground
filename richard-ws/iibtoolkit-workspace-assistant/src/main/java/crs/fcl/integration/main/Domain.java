package crs.fcl.integration.main;

public class Domain {
	private String projectName;
	private String projectGroupId;
	private String projectArtifactId;
	private String projectVersion;
	private String workspaceName;
	private String workspaceRelPath;
	private String worktreesRelPath;
	
		public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getProjectGroupId() {
		return projectGroupId;
	}
	public void setProjectGroupId(String projectGroupId) {
		this.projectGroupId = projectGroupId;
	}
	public String getProjectArtifactId() {
		return projectArtifactId;
	}
	public void setProjectArtifactId(String projectArtifactId) {
		this.projectArtifactId = projectArtifactId;
	}
	public String getProjectVersion() {
		return projectVersion;
	}
	public void setProjectVersion(String projectVersion) {
		this.projectVersion = projectVersion;
	}
	public String getWorkspaceName() {
		return workspaceName;
	}
	public void setWorkspaceName(String workspaceName) {
		this.workspaceName = workspaceName;
	}
	public String getWorkspaceRelPath() {
		return workspaceRelPath;
	}
	public void setWorkspaceRelPath(String workspaceRelPath) {
		this.workspaceRelPath = workspaceRelPath;
	}
	public String getWorktreesRelPath() {
		return worktreesRelPath;
	}
	public void setWorktreesRelPath(String worktreesRelPath) {
		this.worktreesRelPath = worktreesRelPath;
	}
	
}	
	